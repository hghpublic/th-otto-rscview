int unifont_check(void)
{
	unsigned int i;
	unsigned short unicode;
	int errors;
	char *used09;
	char *used10;

	errors = 0;
	used09 = calloc(1, uni09_font.last_ade - uni09_font.first_ade + 1);
	used10 = calloc(1, uni10_font.last_ade - uni10_font.first_ade + 1);
	if (used09 == NULL || used10 == NULL)
	{
		fprintf(stderr, strerror(errno));
		return FALSE;
	}
	for (i = 0; i < ARRAY_SIZE(unicode_table); i++)
	{
		unicode = unicode_table[i].code;
		if (unicode < uni10_font.first_ade || unicode > uni10_font.last_ade ||
			(uni10_font.off_table[unicode - uni10_font.first_ade + 1] - uni10_font.off_table[unicode - uni10_font.first_ade]) == 0)
		{
			fprintf(stderr, "error: unicode 0x%04x does not exist in font10\n", unicode);
			errors++;
		} else
		{
			used10[unicode - uni10_font.first_ade] = 1;
		}
		if (unicode < uni09_font.first_ade || unicode > uni09_font.last_ade ||
			(uni09_font.off_table[unicode - uni09_font.first_ade + 1] - uni09_font.off_table[unicode - uni09_font.first_ade]) == 0)
		{
			fprintf(stderr, "error: unicode 0x%04x does not exist in font09\n", unicode);
			errors++;
		} else
		{
			used09[unicode - uni09_font.first_ade] = 1;
		}
	}
	used09[F_REPLACEMENT_CHAR - uni09_font.first_ade] = 1;
	used10[F_REPLACEMENT_CHAR - uni10_font.first_ade] = 1;
	for (unicode = uni09_font.first_ade; unicode <= uni09_font.last_ade; unicode++)
	{
		if (used09[unicode - uni09_font.first_ade] == 0 && (uni09_font.off_table[unicode - uni09_font.first_ade + 1] - uni09_font.off_table[unicode - uni09_font.first_ade]) != 0)
			fprintf(stderr, "warning: unicode 0x%04x in font09 is unused\n", unicode);
	}
	for (unicode = uni10_font.first_ade; unicode <= uni10_font.last_ade; unicode++)
	{
		if (used10[unicode - uni10_font.first_ade] == 0 && (uni10_font.off_table[unicode - uni10_font.first_ade + 1] - uni10_font.off_table[unicode - uni10_font.first_ade]) != 0)
			fprintf(stderr, "warning: unicode 0x%04x in font10 is unused\n", unicode);
	}

	return errors == 0;
}

