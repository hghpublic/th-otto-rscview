const char *unicode_name(unsigned short unicode)
{
	unsigned int i;
	
	for (i = 0; i < ARRAY_SIZE(unicode_table); i++)
	{
		if (unicode == unicode_table[i].code)
		{
			return unicode_table[i].name;
		}
	}
	return "<unknown>";
}

